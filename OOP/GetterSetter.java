public class GetterSetter {
    private String name;
    private int age;

    // getter and setter
    public String getName(){
        return name;
    } 

    public void setName(String name){
        this.name=name;
    }
        
    // getter for age field
    public int getAge(){
        return age;
    }

    // setter for age field
    public void setAge(int age){
        this.age= age;
    }
}